public class tycat
{
    public static void main(String[] args) {
        
        int a = 10 ;

        // wide casting 
        double b = a ;
        System.out.println(a);
        System.out.println(b);

        // Narrow casting
        double c = (double) a ;
        System.out.println(a);
        System.out.println(c);
    }
    
}
